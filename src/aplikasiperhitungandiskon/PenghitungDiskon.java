    private void pilihDiskonItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_pilihDiskonItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            // Ketika item combo box dipilih, sinkronkan sliderDiskon
            String selected = (String) pilihDiskon.getSelectedItem();
            int diskon = Integer.parseInt(selected.replace("%", ""));
            sliderDiskon.setValue(diskon); // Set nilai slider sesuai persentase
        }
    }//GEN-LAST:event_pilihDiskonItemStateChanged

