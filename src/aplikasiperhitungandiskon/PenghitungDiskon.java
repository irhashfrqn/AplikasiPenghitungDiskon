    private int counter = 1;
    private void hitungBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungBtnActionPerformed
        try {
        // Mengambil input hargaAwal dan menghapus koma sebagai pemisah ribuan
        String hargaText = hargaAwal.getText().replace(",", "").trim();
        
        // Memastikan input tidak kosong
        if (hargaText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harga tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //harga asli
        double harga = Double.parseDouble(hargaText);
        //diskon dari slider
        int diskonPersen = sliderDiskon.getValue();
        //ambil kode voucher dari inputkodeVoucher
        String kodeVoucherInput = inputkodeVoucher.getText().trim();
        int diskonVoucher = cekKodeVoucher(kodeVoucherInput);
        
        //jumlahkan diskon dari slider dan dari voucher
        int totalDiskonPersen = diskonPersen + diskonVoucher;
        
        double diskon = harga * totalDiskonPersen / 100;
        double potonganvoucher = harga * diskonVoucher /100;
        double hargaSetelahDiskon = harga - diskon;

        // Format harga jadi format uang
        DecimalFormat df = new DecimalFormat("#,###.00");
        String hargaawaldf = df.format(harga);
        String hargadf = df.format(hargaSetelahDiskon);
        String diskondf = df.format(diskon);
        String diskonvoucherdf = df.format(potonganvoucher);

        hargaHasil.setText(hargadf);
        potonganHarga.setText(diskondf);
        potonganHargaVoucher.setText(diskonvoucherdf); 
        
        String timeStamp = new SimpleDateFormat("HH:mm:ss").format(new Date()); // Waktu saat perhitungan
        String riwayat = String.format(
            "%s - Harga #%d-----------------\n\n" +
            "Harga Asli : Rp %s\n" +
            "Diskon Persen : %d%%\n" +
            "Voucher : %s (%d%%)\n" +
            "Anda Hemat : Rp %s\n\n" +
            "Harga Akhir : Rp %s\n" +
            "--------------------------------------------\n\n",
            timeStamp, counter, hargaawaldf, diskonPersen, kodeVoucherInput, diskonVoucher, diskondf, hargadf);
        
        // Menambahkan riwayat perhitungan ke JTextArea
        riwayatHitung.append(riwayat);
        counter++;
        
        } catch (NumberFormatException ex) {
            // Menangani input yang tidak valid
            JOptionPane.showMessageDialog(this, "Harga tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hitungBtnActionPerformed

